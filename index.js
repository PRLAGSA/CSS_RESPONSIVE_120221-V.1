function burgerOpen() {
  var x = document.getElementById("sideMenuOne");
  if (x.style.display === "none" || x.style.display == "") {
    x.style.display = "block";
  } else {
    x.style.display = "none";
  }
}

function sideMenuClose() {
  var x = document.getElementById("sideMenuOne");
  if (x.style.display === "none" || x.style.display == "") {
    x.style.display = "block";
  } else {
    x.style.display = "none";
  }
}
